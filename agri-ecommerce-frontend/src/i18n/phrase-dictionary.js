export function translateText(value, locale) {
  if (locale !== "en" || value === null || value === undefined) {
    return value;
  }

  const raw = String(value);
  const trimmed = raw.trim();

  if (!trimmed) {
    return raw;
  }

  const normalized = trimmed.replace(/\s+/g, " ");
  let translated =
    getExactTranslation(normalized) || matchPattern(normalized) || matchStandaloneTerm(normalized);

  if (translated && hasVietnameseText(translated)) {
    translated = fallbackTranslateVietnamese(translated);
  }

  if ((!translated || translated === normalized) && hasVietnameseText(normalized)) {
    translated = fallbackTranslateVietnamese(normalized);
  }

  if (!translated || translated === normalized) {
    return raw;
  }

  const prefix = raw.match(/^\s*/)?.[0] || "";
  const suffix = raw.match(/\s*$/)?.[0] || "";
  return `${prefix}${translated}${suffix}`;
}

export function hasVietnameseText(value) {
  return VIETNAMESE_CHARACTER_PATTERN.test(String(value || ""));
}

function fallbackTranslateVietnamese(value) {
  const currencyText = translateVietnameseCurrency(value);
  if (currencyText) {
    return currencyText;
  }

  const normalized = stripVietnameseAccents(value).replace(/\s+/g, " ").trim();
  const lower = normalized.toLowerCase();
  const replaced = cleanupEnglishFallback(replaceFallbackSegments(lower));

  if (replaced && replaced !== lower && !looksLikeVietnameseAscii(replaced)) {
    return sentenceCaseFallback(replaced);
  }

  if (looksLikeProperNoun(normalized)) {
    return normalized;
  }

  if (!looksLikeVietnameseAscii(lower)) {
    return normalized;
  }

  return classifyVietnameseFallback(lower);
}

function translateVietnameseCurrency(value) {
  const normalized = String(value || "").replace(VIETNAMESE_CURRENCY_PATTERN, " VND");
  if (normalized === String(value || "") || hasVietnameseText(normalized)) {
    return null;
  }
  return normalized.replace(/\s+\/\s+/g, " / ").replace(/\s{2,}/g, " ").trim();
}

function stripVietnameseAccents(value) {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\u0111/g, "d")
    .replace(/\u0110/g, "D")
    .normalize("NFC");
}

function replaceFallbackSegments(value) {
  let result = value;
  for (const [source, replacement] of fallbackSegmentTranslations) {
    result = result.replace(new RegExp(`\\b${escapeRegExp(source)}\\b`, "g"), replacement);
  }
  return result;
}

function cleanupEnglishFallback(value) {
  return String(value || "")
    .replace(/\s+([.,;:!?])/g, "$1")
    .replace(/\s{2,}/g, " ")
    .trim();
}

function sentenceCaseFallback(value) {
  if (!value) {
    return value;
  }
  return `${value.charAt(0).toUpperCase()}${value.slice(1)}`;
}

function looksLikeVietnameseAscii(value) {
  return VIETNAMESE_ASCII_WORD_PATTERN.test(String(value || ""));
}

function looksLikeProperNoun(value) {
  const text = String(value || "").trim();
  if (!text || /[.,;:!?]/.test(text)) {
    return false;
  }
  return /^[A-Z][A-Za-z]*(?:\s+[A-Z][A-Za-z]*){1,5}$/.test(text);
}

function classifyVietnameseFallback(value) {
  if (ADDRESS_ASCII_WORD_PATTERN.test(value)) {
    return "Localized address";
  }
  if (PRODUCT_ASCII_WORD_PATTERN.test(value)) {
    return "Localized product information";
  }
  if (REVIEW_ASCII_WORD_PATTERN.test(value)) {
    return "Localized review information";
  }
  if (ORDER_ASCII_WORD_PATTERN.test(value)) {
    return "Localized order information";
  }
  return String(value || "").length > 80 ? "Localized content unavailable" : "Localized content";
}

function escapeRegExp(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function matchPattern(value) {
  for (const [pattern, replacement] of patterns) {
    const match = value.match(pattern);
    if (match) {
      if (typeof replacement === "function") {
        return replacement(...match);
      }
      return value.replace(pattern, replacement);
    }
  }
  return null;
}

function translateTerm(value) {
  const normalized = String(value || "")
    .trim()
    .replace(/\s+/g, " ")
    .toLowerCase();

  return termTranslations[normalized] || getExactTranslation(String(value).trim()) || String(value).trim();
}

function matchStandaloneTerm(value) {
  const translated = translateTerm(value);
  if (!translated || translated === value) {
    return null;
  }

  return startsWithUppercase(value) ? capitalize(translated) : translated;
}

function capitalize(value) {
  if (!value) {
    return value;
  }
  return `${value.charAt(0).toUpperCase()}${value.slice(1)}`;
}

function startsWithUppercase(value) {
  return /^[A-ZÀ-ỸĐ]/.test(value);
}

function getExactTranslation(value) {
  return exactTranslations[value] || additionalTranslations[value] || null;
}
